    private void formWindowOpened(java.awt.event.WindowEvent evt)//GEN-FIRST:event_formWindowOpened
    {//GEN-HEADEREND:event_formWindowOpened
        DefaultTableModel model = (DefaultTableModel) desp.getModel();
        model.setColumnIdentifiers(new Object[]
        {
            "Nombre", "Primer Apellido", "Segundo Apellido", "Padecimiento", "Antecedentes", "Medicamento", "Tratamiento", "Fecha"
        });

        JTableHeader header = desp.getTableHeader();
        header.setReorderingAllowed(false);

        JTableHeader header1 = desp.getTableHeader();
        header.setResizingAllowed(false);
        try
        {
            DefaultTableModel dtm = (DefaultTableModel) desp.getModel();
            String cvePaciente = JOptionPane.showInputDialog("Ingrese la clave del paciente:");
            int index = Manipulacion.buscarcve(VtnInicio.d, cvePaciente);

            if (index != -1 && VtnInicio.d[index] instanceof Alumnos)
            {
                // Mostrar las consultas del paciente
                for (int j = 0; j < VtnInicio.matriz[index].length; j++)
                {
                    HistorialClinico consulta = VtnInicio.matriz[index][j];
                    Object[] rowData =
                    {
                        VtnInicio.d[index].getNom(),
                        VtnInicio.d[index].getPrimerAp(),
                        VtnInicio.d[index].getSegundoAp(),
                        consulta.getPadecimientoAct(),
                        consulta.getAntededentesPer(),
                        consulta.getMedicamento(),
                        consulta.getPlanTratamiento(),
                        consulta.getFecha()
                    };
                    dtm.addRow(rowData);
                }
            } else
            {
                Mensaje.error(this, "No se encontrÃ³ el paciente con clave " + cvePaciente);
            }
        } catch (Exception e)
        {
            Mensaje.error(this, "El CVE ingresado no cuenta con consultas registradas");
        }
    }//GEN-LAST:event_formWindowOpened

